    def __init__(self, name):
        """Constructor for Children."""
        self.name = name
        self.age = randint(1, 11)
        self.lump_of_coal = None
        self.good_present = None

        judge = randint(0, 2)
        if judge == 0:
            self.naughty_or_nice = "Naughty"
        else:
            self.naughty_or_nice = "Nice"

    def __repr__(self):
        """Represent each Child object with its name."""
        return self.name


